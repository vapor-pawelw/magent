// MARK: - SemanticVersion

@Suite("SemanticVersion parsing")
struct SemanticVersionTests {

    @Test("Parses full x.y.z version")
    func parsesFullVersion() throws {
        let v = try #require(SemanticVersion("1.2.3"))
        #expect(v.major == 1 && v.minor == 2 && v.patch == 3)
    }

    @Test("Accepts leading v/V prefix")
    func parsesLeadingV() throws {
        #expect(SemanticVersion("v1.2.3")?.displayString == "1.2.3")
        #expect(SemanticVersion("V2.0.0")?.displayString == "2.0.0")
    }

    @Test("Defaults missing components to 0")
    func parsesShortForm() throws {
        let major = try #require(SemanticVersion("4"))
        #expect(major.major == 4 && major.minor == 0 && major.patch == 0)

        let majMinor = try #require(SemanticVersion("4.5"))
        #expect(majMinor.minor == 5 && majMinor.patch == 0)
    }

    @Test("Strips pre-release / build suffix after first dash")
    func stripsSuffix() throws {
        let v = try #require(SemanticVersion("1.2.3-alpha.4"))
        #expect(v.major == 1 && v.minor == 2 && v.patch == 3)
    }

    @Test("Trims surrounding whitespace")
    func trimsWhitespace() throws {
        #expect(SemanticVersion("  1.0.0 \n")?.displayString == "1.0.0")
    }

    @Test("Rejects non-numeric components")
    func rejectsNonNumeric() {
        #expect(SemanticVersion("abc") == nil)
        #expect(SemanticVersion("1.x.0") == nil)
        #expect(SemanticVersion("") == nil)
    }

    @Test("Comparable orders major/minor/patch correctly")
    func comparable() throws {
        let a = try #require(SemanticVersion("1.2.3"))
        let b = try #require(SemanticVersion("1.2.4"))
        let c = try #require(SemanticVersion("1.3.0"))
        let d = try #require(SemanticVersion("2.0.0"))
        #expect(a < b)
        #expect(b < c)
        #expect(c < d)
        #expect(!(d < a))
    }
}

