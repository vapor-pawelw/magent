@Suite("TmuxSessionNaming")
struct TmuxSessionNamingTests {

    @Test("sanitizeForTmux replaces illegal chars with hyphens and lowercases")
    func sanitize() {
        #expect(TmuxSessionNaming.sanitizeForTmux("My Project!") == "my-project-")
        #expect(TmuxSessionNaming.sanitizeForTmux("already_ok-123") == "already_ok-123")
