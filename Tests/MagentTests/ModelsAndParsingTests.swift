// MARK: - GitRemote URL builders

@Suite("GitRemote PR URL builders")
struct GitRemotePullRequestURLTests {

    let github = GitRemote.parse(name: "origin", rawURL: "https://github.com/owner/repo.git")!
    let gitlab = GitRemote.parse(name: "origin", rawURL: "https://gitlab.com/group/repo.git")!
    let bitbucket = GitRemote.parse(name: "origin", rawURL: "https://bitbucket.org/team/repo.git")!
    let unknown = GitRemote.parse(name: "origin", rawURL: "https://git.example.com/o/r.git")!

    @Test("Default branch returns open PRs listing")
    func defaultBranchGoesToListing() {
        let url = github.pullRequestURL(for: "main", defaultBranch: "main")
        #expect(url?.absoluteString == "https://github.com/owner/repo/pulls?q=is%3Aopen+is%3Apr")
    }

    @Test("Branch-specific URL for GitHub includes head:<branch> query")
    func githubBranchQuery() {
        let url = github.pullRequestURL(for: "feature x", defaultBranch: "main")
        let encoded = url?.absoluteString ?? ""
        // urlQueryAllowed encodes spaces but not '/', matching GitHub's expected query format.
        #expect(encoded.contains("head%3Afeature%20x"))
    }

    @Test("GitLab uses source_branch query")
    func gitlabBranchQuery() {
        let url = gitlab.pullRequestURL(for: "feature", defaultBranch: "main")
        #expect(url?.absoluteString.contains("source_branch=feature") == true)
        #expect(url?.absoluteString.contains("state=opened") == true)
    }

    @Test("Bitbucket uses source query and state=OPEN")
    func bitbucketBranchQuery() {
        let url = bitbucket.pullRequestURL(for: "feature", defaultBranch: "main")
        #expect(url?.absoluteString.contains("source=feature") == true)
        #expect(url?.absoluteString.contains("state=OPEN") == true)
    }

    @Test("createPullRequestURL requires non-empty source")
    func createPRRequiresSource() {
        #expect(github.createPullRequestURL(sourceBranch: "   ", targetBranch: "main") == nil)
    }

    @Test("GitHub createPullRequestURL requires target and uses compare path")
    func createPRGitHub() throws {
        let url = try #require(github.createPullRequestURL(sourceBranch: "feature", targetBranch: "main", title: "My PR"))
        #expect(url.absoluteString.hasPrefix("https://github.com/owner/repo/compare/main...feature"))
        #expect(url.absoluteString.contains("expand=1"))
        #expect(url.absoluteString.contains("title=My%20PR"))

        #expect(github.createPullRequestURL(sourceBranch: "feature", targetBranch: nil) == nil)
    }

    @Test("GitLab createPullRequestURL builds merge_request form")
    func createPRGitLab() throws {
        let url = try #require(gitlab.createPullRequestURL(sourceBranch: "feature", targetBranch: "main", title: "t"))
        let s = url.absoluteString
        #expect(s.contains("merge_request%5Bsource_branch%5D=feature"))
        #expect(s.contains("merge_request%5Btarget_branch%5D=main"))
        #expect(s.contains("merge_request%5Btitle%5D=t"))
    }

    @Test("Bitbucket createPullRequestURL requires target")
    func createPRBitbucket() throws {
        #expect(bitbucket.createPullRequestURL(sourceBranch: "feature", targetBranch: nil) == nil)
        let url = try #require(bitbucket.createPullRequestURL(sourceBranch: "feature", targetBranch: "main"))
        let s = url.absoluteString
        #expect(s.contains("source=feature"))
        #expect(s.contains("dest=main"))
    }

    @Test("Unknown provider returns nil for createPullRequestURL")
    func createPRUnknown() {
        #expect(unknown.createPullRequestURL(sourceBranch: "feature", targetBranch: "main") == nil)
    }

    @Test("directPullRequestURL varies by provider")
    func directURLs() {
        #expect(github.directPullRequestURL(number: 42)?.absoluteString == "https://github.com/owner/repo/pull/42")
        #expect(gitlab.directPullRequestURL(number: 42)?.absoluteString == "https://gitlab.com/group/repo/-/merge_requests/42")
        #expect(bitbucket.directPullRequestURL(number: 42)?.absoluteString == "https://bitbucket.org/team/repo/pull-requests/42")
        #expect(unknown.directPullRequestURL(number: 42) == nil)
    }
}

