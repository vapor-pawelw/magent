        #expect(name == "Claude (Sonnet 4.6, H)")
    }

    @Test("Codex strips GPT prefix and hyphenates tokens")
    func codexModel() {
        let name = TmuxSessionNaming.defaultTabDisplayName(for: .codex, modelLabel: "GPT 5.3 Codex")
        #expect(name == "Codex (5.3-codex)")
    }

    @Test("Codex collapses multiple spaces and trims")
    func codexCollapse() {
        let name = TmuxSessionNaming.defaultTabDisplayName(for: .codex, modelLabel: "  gpt   5.4-mini  ")
        #expect(name == "Codex (5.4-mini)")
    }

    @Test("Empty/whitespace model label is ignored")
    func emptyModelIgnored() {
        #expect(TmuxSessionNaming.defaultTabDisplayName(for: .claude, modelLabel: "   ") == "Claude")
        #expect(TmuxSessionNaming.defaultTabDisplayName(for: .claude, modelLabel: nil) == "Claude")
    }

    @Test("Custom agent ignores model label")
    func customIgnoresModel() {
        #expect(TmuxSessionNaming.defaultTabDisplayName(for: .custom, modelLabel: "whatever") == "Custom")
    }

    @Test("looksLikeDefaultTabName matches exact base")
    func looksLikeBase() {
        #expect(TmuxSessionNaming.looksLikeDefaultTabName("Claude", for: .claude))
        #expect(TmuxSessionNaming.looksLikeDefaultTabName("Codex", for: .codex))
        #expect(TmuxSessionNaming.looksLikeDefaultTabName("Terminal", for: nil))
    }

    @Test("looksLikeDefaultTabName matches base with valid parenthesised detail")
    func looksLikeWithDetail() {
        #expect(TmuxSessionNaming.looksLikeDefaultTabName("Claude (M)", for: .claude))
        #expect(TmuxSessionNaming.looksLikeDefaultTabName("Claude (Sonnet 4.6)", for: .claude))
        #expect(TmuxSessionNaming.looksLikeDefaultTabName("Claude (Sonnet 4.6, H)", for: .claude))
        #expect(TmuxSessionNaming.looksLikeDefaultTabName("Codex (5.3-codex)", for: .codex))
    }

    @Test("looksLikeDefaultTabName rejects human-meaningful names")
    func looksLikeRejects() {
